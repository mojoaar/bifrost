export const PALETTE_MAP = new Map(PALETTES.map((p) => [p.slug, p]));

export function cssForPalette(p: Palette): string {
  const blocks: string[] = [];
  for (const mode of ["light", "dark"] as const) {
    const tokens = p[mode];
    const lines: string[] = [];
    for (const [key, value] of Object.entries(tokens)) {
      lines.push(`    ${key}: ${value};`);
    }
    blocks.push(`:root[data-palette="${p.slug}"][data-theme="${mode}"] {\n${lines.join("\n")}\n  }`);
  }
  return blocks.join("\n\n");
}

export const CSS_VARIABLE_TOKENS: string[] = (() => {
  const keys = new Set<string>();
  for (const p of PALETTES) {
    for (const mode of ["light", "dark"] as const) {
      for (const k of Object.keys(p[mode])) keys.add(k);
    }
  }
  return [...keys].sort();
})();
